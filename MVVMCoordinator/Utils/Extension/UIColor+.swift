

import UIKit

extension UIColor{
    func greenColor() -> UIColor {
      return  UIColor(red: 2.0/255.0, green: 202.0/255.0, blue: 185.0/255.0, alpha: 1)
        
    }
    func grayColor() -> UIColor {
      return  UIColor(red: 182.0/255.0, green: 182.0/255.0, blue: 182.0/255.0, alpha: 1)
    }
}
